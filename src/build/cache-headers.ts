import { toViewPathWithBase } from "../view-contract";
import type { RuntimeAssets } from "./contracts";

export const IMMUTABLE_CACHE_CONTROL = "public, max-age=31536000, immutable";
export const REVALIDATE_CACHE_CONTROL = "public, max-age=0, must-revalidate";

function mutablePathPatterns(pathBase: string): string[] {
  const baseRoot = toViewPathWithBase("/", pathBase);
  if (baseRoot === "/") {
    return ["/*"];
  }

  return [baseRoot.slice(0, -1), `${baseRoot}*`];
}

function immutableRuntimePaths(runtimeAssets: RuntimeAssets): string[] {
  return Array.from(
    new Set(
      [
        runtimeAssets.cssRelPath,
        runtimeAssets.jsRelPath,
        runtimeAssets.treeJsRelPath,
        runtimeAssets.mermaidJsRelPath,
      ].filter((assetPath): assetPath is string => Boolean(assetPath)),
    ),
  ).sort();
}

export function renderCloudflarePagesHeaders(
  runtimeAssets: RuntimeAssets,
  pathBase: string,
): string {
  const lines = [
    "# Generated by Everything Is A Markdown for Cloudflare Pages.",
    "# HTML, manifest.json, content fragments, SEO metadata, and copied files revalidate.",
  ];

  for (const pattern of mutablePathPatterns(pathBase)) {
    lines.push("", pattern, `  Cache-Control: ${REVALIDATE_CACHE_CONTROL}`);
  }

  lines.push(
    "",
    "# Only build-owned, content-hashed runtime assets are immutable.",
    "# Each exact rule replaces the broader revalidation header.",
  );
  for (const assetPath of immutableRuntimePaths(runtimeAssets)) {
    lines.push(
      "",
      toViewPathWithBase(`/${assetPath}`, pathBase),
      "  ! Cache-Control",
      `  Cache-Control: ${IMMUTABLE_CACHE_CONTROL}`,
    );
  }

  return `${lines.join("\n")}\n`;
}
